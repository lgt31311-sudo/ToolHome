// í´ë¦½ë³´ë“œ ë³µì‚¬
async function copyToClipboard(text, button) {
  try {
    await navigator.clipboard.writeText(text);
    showCopiedFeedback(button);
    showToast();
  } catch (err) {
    // í´ë°±
    const textarea = document.createElement('textarea');
    textarea.value = text;
    document.body.appendChild(textarea);
    textarea.select();
    document.execCommand('copy');
    document.body.removeChild(textarea);
    showCopiedFeedback(button);
    showToast();
  }
}

// ë²„íŠ¼ í”¼ë“œë°±
function showCopiedFeedback(button) {
  button.classList.add('copied');
  setTimeout(() => {
    button.classList.remove('copied');
  }, 500);
}

// í† ìŠ¤íŠ¸ í‘œì‹œ
function showToast() {
  toast.classList.add('show');
  setTimeout(() => {
    toast.classList.remove('show');
  }, 1500);
}

// ìµœê·¼ ë³µì‚¬ ì¶”ê°€
function addToRecent(char, name) {
  // ì¤‘ë³µ ì œê±°
  recentSymbols = recentSymbols.filter(s => s.char !== char);
  // ì•ì— ì¶”ê°€
  recentSymbols.unshift({ char, name });
  // ìµœëŒ€ 10ê°œ ìœ ì§€
  if (recentSymbols.length > 10) {
    recentSymbols = recentSymbols.slice(0, 10);
  }
  // ì €ì¥
  localStorage.setItem('recentSymbols', JSON.stringify(recentSymbols));
  renderRecentSymbols();
}

// ìµœê·¼ ë³µì‚¬ ë Œë”ë§
function renderRecentSymbols() {
  if (recentSymbols.length === 0) {
    recentSection.style.display = 'none';
    return;
  }

  recentSection.style.display = 'block';
  recentGrid.innerHTML = recentSymbols.map(s => createSymbolButton(s)).join('');
}

